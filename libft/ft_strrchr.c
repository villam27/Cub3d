char	*ft_strrchr(const char *s, int c)
{
	unsigned int	i;
	char			*p;

	i = 0;
	p = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			p = &((char *)s)[i];
		i++;
	}
	if (!(char)c)
		p = &((char *)s)[i];
	return (p);
}
