char	*ft_strchr(const char *s, int c)
{
	unsigned int	i;

	i = 0;
	while (s[i] && s[i] != (char)c)
		i++;
	if (s[i] || (!s[i] && (char)c == 0))
		return (&((char *)s)[i]);
	else
		return (0);
}
